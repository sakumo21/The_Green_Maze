#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	l;

	l = ft_strlen(s);
	i = 0;
	while (s[i])
	{
		if ((unsigned char)s[i] == (unsigned char)c)
			return ((char *)s + i);
		i++;
	}
	if ((char)c == s[i])
		return ((char *)s + l);
	return (NULL);
}
